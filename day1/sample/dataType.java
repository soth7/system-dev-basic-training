public class dataType {
  public static void main(String[] args) {
      // 真偽値型の例
      boolean isJavaFun = true;
      System.out.println("Boolean example: " + isJavaFun);

      // 文字型の例
      char grade = 'A';
      System.out.println("Char example: " + grade);

      // 文字列型の例
      String greeting = "Hello, World!";
      System.out.println("String example: " + greeting);

      // 整数型の例
      byte smallNumber = 100;
      short mediumNumber = 10000;
      int number = 123456789;
      long largeNumber = 123456789012345L;
      System.out.println("Byte example: " + smallNumber);
      System.out.println("Short example: " + mediumNumber);
      System.out.println("Int example: " + number);
      System.out.println("Long example: " + largeNumber);

      // 小数型の例
      float piFloat = 3.14f;
      double piDouble = 3.141592653589793;
      System.out.println("Float example: " + piFloat);
      System.out.println("Double example: " + piDouble);
  }
}
